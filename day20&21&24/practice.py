# list[index1: index2], this is how to slice a list to get the values between index1 and index2
# example : keys = ["a", "b", "c", "d", "e", "f", "g"], keys[2:5] will return c, d, e. From 2 to 5, excluding 5. 
# keys[2:5:2], this will do the same but select one, skip one element. if its 3, it skips 2 elements and so on.
